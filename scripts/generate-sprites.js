function hexToRgba(hex) {
  const r = parseInt(hex.slice(1, 3), 16);
  const g = parseInt(hex.slice(3, 5), 16);
  const b = parseInt(hex.slice(5, 7), 16);
  return { r, g, b, a: 255 };
}

const outputDir = path.join(__dirname, '..', 'public', 'characters');

for (const [id, char] of Object.entries(pixelCharacters)) {
  const sprite = char.sprite;
  let maxW = 0;
  for (const row of sprite) {
    if (row.length > maxW) maxW = row.length;
  }
  const cols = maxW;
  const rows = sprite.length;

  const png = new PNG({ width: cols, height: rows });

  for (let y = 0; y < rows; y++) {
    const line = sprite[y];
    for (let x = 0; x < cols; x++) {
      const idx = line && x < line.length ? line[x] : '.';
      const pi = y * cols + x;
      if (idx === '.') {
        png.data[pi * 4] = 0;
        png.data[pi * 4 + 1] = 0;
        png.data[pi * 4 + 2] = 0;
        png.data[pi * 4 + 3] = 0;
      } else {
        const color = char.paleta[parseInt(idx)];
        if (color) {
          const c = hexToRgba(color);
          png.data[pi * 4] = c.r;
          png.data[pi * 4 + 1] = c.g;
          png.data[pi * 4 + 2] = c.b;
          png.data[pi * 4 + 3] = c.a;
        }
      }
    }
  }

  const buffer = PNG.sync.write(png);
  const filePath = path.join(outputDir, `${id}.png`);
  fs.writeFileSync(filePath, buffer);
  console.log(`Generated: ${id}.png (${cols}x${rows})`);
}

